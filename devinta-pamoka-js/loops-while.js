// WHILE loop (ciklas)
// Vykdome skaičiavimus ir norime juos vykdyti iki kol pasieksime tinkamą rezultatą.
// Suks ta ciklo rata, kol ta salyga yra teisinga

// pvz 1

let skaicius = 480;

while (skaicius <= 500) {
  console.log(skaicius);
  skaicius++;
}

// pvz 2

let tekstas = 'Hello World';
let kiekLiko = 10;

while (kiekLiko > 0) {
  console.log(tekstas);
  kiekLiko--;
}

// pvz 3

let prekiuKiekis = 30;

while (prekiuKiekis > 0) {
  console.log('Prekiu kiekis: ', prekiuKiekis);
  prekiuKiekis--;
}
