let pavarde = 'Pavarde';
let vardasPavarde = `${vardas} ${pavarde}`;

// Boolean - tai loginis duomenu tipas, kuris gali buti tik true arba false.
let isPasswordValid = true;
let isEmailValid = false;

// Undefined - tai kintamasis, kuriam nebuvo priskirta jokia reiksme.
let akiuSpalva;

// Null - reiksme, kuri reiskia, kad kintamasis yra tuscias, jo reiksme dar nera zinoma.
let automobilioMarke = null;

